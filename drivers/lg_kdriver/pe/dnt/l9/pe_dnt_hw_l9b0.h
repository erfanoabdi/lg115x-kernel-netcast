#define P0L_L9B0_DNT_SET_HMC(_default)	\
	do{\
		UINT32 r_data,w_data;\
		PE_P0L_L9B0_RdFL(ipc_ctrl_23);\
		r_data = PE_P0L_L9B0_Rd(ipc_ctrl_23);\
